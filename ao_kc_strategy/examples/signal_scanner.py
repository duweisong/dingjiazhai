def scan(tdx_path=r"C:\zd_pazq_hy"):
    """扫描沪深300，找出买入信号"""
    buy_signals, holding, wait = [], [], []
    for code in HS300:
        fpath = find_tdx_file(code, tdx_path)
        if not fpath: continue
        try:
            df = read_tdx_day(fpath)
            if len(df) < 100: continue
            df = calc_ao(df); df = calc_kc(df); df = generate(df)
            last = df.iloc[-1]
            if last["pos"] > 0:
                holding.append((code, last["Close"], last["entry"], last["stop"],
                               (last["Close"]/last["entry"]-1)*100, last["ao"]))
            elif last["ao_up"] and not last["below_kc"]:
                buy_signals.append((code, last["Close"], last["ao"], last["kc_low"], last["atr"]))
            else:
                wait.append(code)
        except: pass

    print(f"\n{'='*80}")
    print(f"  Daily Signal Scanner")
    print(f"  Buy: {len(buy_signals)} | Holding: {len(holding)} | Wait: {len(wait)}")
    print(f"{'='*80}")

    if buy_signals:
        print(f"\n  [BUY SIGNALS]")
        for c, cl, ao, kc, atr in sorted(buy_signals, key=lambda x: x[2], reverse=True):
            stop = cl - Cfg.STOP_ATR * atr
            print(f"  {c}  Close={cl:.2f}  AO={ao:+.4f}  KC_low={kc:.2f}  Stop={stop:.2f}")

    if holding:
        at_risk = [(c, cl, e, s, p, a) for c, cl, e, s, p, a in holding if (cl-s)/cl < 0.03]
        if at_risk:
            print(f"\n  [NEAR STOP - {len(at_risk)} positions]")
            for c, cl, e, s, p, a in at_risk:
                print(f"  {c}  Entry={e:.2f}  Close={cl:.2f}  Stop={s:.2f}  Gap={(cl-s)/cl*100:.1f}%")

if __name__ == "__main__":
    scan()
